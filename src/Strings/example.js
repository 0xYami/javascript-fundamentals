// Single quotes
console.log('Hello World');

// Double quotes

console.log("Hello World");

// Backticks

console.log(`Hello World`);

// String concatenation

console.log('Hello' + ' ' + 'World');

// String interpolation

console.log(`Hello ${'World'}`);

// String interpolation with variables

const name = 'John';

console.log(`Hello ${name}`);

// String interpolation with expressions

const a = 5;

const b = 10;

console.log(`Fifteen is ${a + b}`)


